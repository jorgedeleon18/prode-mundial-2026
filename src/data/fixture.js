// Cierre: medianoche del día del partido (23:59 del día anterior ARG)
export const estaAbierto = (partido) => {
  const [y, m, d] = partido.fecha.split('-').map(Number);
  return new Date() < new Date(y, m - 1, d, 0, 0, 0);
};

export const estaFinalizado = (p) =>
  p.gl !== undefined && p.gl !== null && p.gl !== '';

export const fechaCorta = (f) => {
  const [y, m, d] = f.split('-');
  return `${d}/${m}/${y}`;
};

export const cierreStr = (p) => {
  const [y, m, d] = p.fecha.split('-').map(Number);
  const ant = new Date(y, m - 1, d - 1);
  return `${String(ant.getDate()).padStart(2,'0')}/${String(ant.getMonth()+1).padStart(2,'0')} 23:59`;
};

// Sistema de puntaje AUBASA
// 5=exacto | 3=ganador+diferencia | 1=solo ganador | 0=nada
export const calcPts = (partido, prono) => {
  const rl = parseInt(partido.gl), rv = parseInt(partido.gv);
  const pl = parseInt(prono.gL), pv = parseInt(prono.gV);
  if ([rl,rv,pl,pv].some(isNaN)) return null;
  const rR = rl>rv?'L':rl<rv?'V':'E';
  const rP = pl>pv?'L':pl<pv?'V':'E';
  if (pl===rl && pv===rv) return { pts:5, lbl:'Resultado exacto ✦' };
  if (rR===rP && Math.abs(rl-rv)===Math.abs(pl-pv)) return { pts:3, lbl:'Ganador + diferencia' };
  if (rR===rP) return { pts:1, lbl:'Solo el ganador' };
  return { pts:0, lbl:'Sin puntos' };
};
